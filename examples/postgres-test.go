package main

import (
	"database/sql"
	"fmt"

	_ "github.com/lib/pq"
)

const (
	host     = "localhost"
	port     = 5432
	user     = "postgres"
	password = "admin"
	dbname   = "testDB"
)

func main() {
	psqlInfo := fmt.Sprintf("host=%s port=%d user=%s password=%s dbname=%s sslmode=disable",
		host, port, user, password, dbname)

	db, err := sql.Open("postgres", psqlInfo)
	if err != nil {
		panic(err)
	}
	defer db.Close()

	fmt.Println(db)

	_, err = db.Exec(`CREATE TABLE IF NOT EXISTS test123 (id SERIAL PRIMARY KEY, name VARCHAR(50))`)
	if err != nil {
		panic(err)
	}

	fmt.Println("Successfully connected and created table!")
}
